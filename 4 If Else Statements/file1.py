# Conditional Tests
a = input("Enter your number: ")

# ==
if a == 29:
    print(bool(a))
else:
    print(a)


# !=
if a != 29:
    print(bool(a))
else:
    print(a)


# >
if a > 29:
    print(bool(a))
else:
    print(a)


# >=
if a >= 29:
    print(bool(a))
else:
    print(a)


# <
if a < 29:
    print(bool(a))
else:
    print(a)


# <=
if a <= 29:
    print(bool(a))
else:
    print(a)